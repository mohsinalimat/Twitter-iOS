import UIKit

extension UIColor {
    
    class func primaryBlue() -> UIColor {
        return UIColor(red: 30/255, green: 149/255, blue: 242/255, alpha: 1)
    }
    
    class func primaryGreen() -> UIColor {
        return UIColor(red: 25/255, green: 201/255, blue: 140/255, alpha: 1)
    }
    
}
